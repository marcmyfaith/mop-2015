ise and worship to honor the Lord, and His commitment to His people.  Monument has acquired our present location 1212 Pearson Place to house the growing congregation.  In April 2014, Monument purchased an additional 21K square feet church located at 321 Oakview Drive in  High Point.Other notable achievements include the purchase of a van to implement the church’s transportation ministry, extensive soul-winning outreach, and a weekly meal ministry that serves the surrounding community.  For many years, Monument has been in pursuit of reaching souls for Christ through outreach and service and as a result has been coined in the community as “The Monument.” With God’s help we will do great things for the Kingdom of God because “there is no limit to a committed people.”</p>
